package models;

public class Marca {

    private int idMarca;
    private String nome;

    public Marca() {
    }

    public int getIdMarca() {
        return idMarca;
    }

    public String getNome() {
        return nome;
    }

	public void setIdMarca(int idMarca) {
		this.idMarca = idMarca;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
    
    
}

